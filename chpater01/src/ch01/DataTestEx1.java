package ch01;

public class DataTestEx1 {

	public static void main(String[] args) {
		// 1. 정수 500을 name1 이름으로 변수에 담기
		int  name1 = 550;
		// 2. 정수 22억을 name2 이름으로 변수에 담기
		long name2 = 2200000000L;
		// 3. 문자 A를 name3 이름으로 변수에 담기
		char name3 = 'A';
		// 4. 실수 float 타입으로 0.12345678을 name4 이름으로 변수에 담기
		float name4 = 0.12345678F;
		// 5. 실수 double 타입으로 200.0035을 name5 이름으로 변수에 담기
		double name5 = 200.0035;
		// 6. 논리 자료형 isOk 이름의 변수를 선언하기
		boolean isOk;
		// 7. isOk 변수에 값을 false로 초기화하기
		isOk = false;
		// 8. isOk 변수에 값을 true로 변경하기
		isOk = true;
		// 9. 변수란 무엇인가?
		// 변수란 말 그대로 변하는 수이다. 프로그래밍에 있어 데이터 타입에 따라 메모리를 할당받는 크기가 다르다. 할당받은 메모리를 변수로 접근한다.
		// 10. 기본 데이터 타입에 종류 8가지 서술
		// 정수형 : byte, short, int, long
		// 실수형 : float, double
		// 문자형 : char
		// 논리형 : boolean

	}

}
